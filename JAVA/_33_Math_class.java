class _33_Math_class
{
	public static void main(String[] args) {
		System.out.println("sin(5.7) = "+Math.sin(5.7));
		System.out.println("asin(1.0) = "+Math.asin(1.0));
		System.out.println("2 pow 3 = "+Math.pow(2,3));
		System.out.println("root of 25 = "+Math.sqrt(25));
		System.out.println("Celling of 5.2 = "+Math.ceil(5.2));
		System.out.println("Floor of 6.7 = "+Math.floor(6.7));
		System.out.println("min from 12, 34 = "+Math.min(12,34));
		System.out.println("max from 12, 34 = "+Math.max(12,34));
		System.out.println("Random val = "+Math.random());
	}
}

