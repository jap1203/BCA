class _06_arithmatic
{
	public static void main(String[] args) {
		int a,b,c;
		a = 25;
		b = 5;
		c = a+b;
		System.out.println("Addition = "+c);
		c = a-b;
		System.out.println("Subtraction = "+c);
		c = a*b;
		System.out.println("Multiplication = "+c);
		c = a/b;
		System.out.println("Divison = "+c);
	}
}

/*
Addition = 30
Subtraction = 20
Multiplication = 125
Divison = 5
*/