class _03_Days_array
{
	public static void main(String[] args) {
		String days[] = {"Monday","Tuesday","Wednesday","Thursday","Friday","Saturday","Sunday"};
		for (int i=0; i<7; i++) {
			System.out.println("The Day "+(i+1)+" is : "+days[i]);
		}
	}
}

/*
The Day 1 is : Monday
The Day 2 is : Tuesday
The Day 3 is : Wednesday
The Day 4 is : Thursday
The Day 5 is : Friday
The Day 6 is : Saturday
The Day 7 is : Sunday
*/